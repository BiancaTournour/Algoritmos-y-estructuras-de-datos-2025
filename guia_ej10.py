def cancion_mas_larga(lista):
    max = 0
    cancion_max = None
    for cancion in lista:
        dur = cancion['duracion']
        if dur > max:
            max = dur
            cancion_max = cancion
    print(f'la cancion mas larga es {cancion_max['cancion']} de {cancion_max['artista']}')

