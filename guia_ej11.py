#listar personajes femeninos
def listar_femeninos(lista):
    temp = List()
    for personaje in lista:
        if personaje['genero'] == 'femenino':
            temp.append(personaje['nombre'])
    if temp:
        print('personajes femeninos:')
        temp.show()
    else: print('no hay personajes femeninos en la lista')


#droides de los primeros 6 episodios
def droides(lista):
    episodios_consulta = [1, 2, 3, 4, 5, 6]
    temp = List()
    for personaje in lista:
        if personaje['especie'] == 'droide' and any(ep in episodios_consulta for ep in personaje['apariciones/episodios']):
            temp.append(personaje['nombre'])
    if temp:
        print('droides que aparecieron en los primeros 6 episodios:')
        temp.show()
    else: print('no hay droides que hayan aparecido en los primeros 6 episodios en la lista')

  
