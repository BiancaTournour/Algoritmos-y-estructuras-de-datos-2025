#personajes mayores a 850 años
def edad_personajes(lista):
    temp = List()
    for personaje in lista:
        if personaje['edad'] > 850:
            temp.append(f'personaje: {personaje['nombre']}, edad: {personaje['edad']}')
    if temp:
        print('personajes mayores a 850 años:')
        temp.show()
    else: print('no hay personajes mayores a 850 años')

#mayor personaje
def mayor_personaje(lista):
    max_edad = 0
    max_personaje = ''
    for personaje in lista:
        if personaje['edad'] > max_edad:
            max_edad = personaje['edad']
            max_personaje = personaje['nombre']
    print(f'el personaje mayor de la lista es {max_personaje} con {max_edad} años')        

#personajes IV, V y VI
def episodios_IV_V_VI(lista):
    temp = List()
    for personaje in lista:
        episodios = personaje['apariciones/episodios']
        if all(ep in [4, 5, 6] for ep in episodios):
            lista.delete_value(personaje)
            temp.append(personaje)
    if temp:
        print('personajes eliminados:')
        temp.show()
        lista.extend(temp) #restauramos la lista
    else: print('no se eliminó a ningun personaje')      

